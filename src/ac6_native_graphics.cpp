void SyncParallelRendererStatus() {
  std::lock_guard<std::mutex> renderer_lock(g_parallel_renderer_mutex);
  const ac6::renderer::NativeRendererStats stats = g_parallel_renderer.GetStats();
  std::lock_guard<std::mutex> status_lock(g_native_graphics_status_mutex);
  UpdateParallelRendererStatusLocked(g_parallel_renderer, stats);
}

void ShutdownParallelRenderer() {
  std::lock_guard<std::mutex> renderer_lock(g_parallel_renderer_mutex);
  g_parallel_renderer.Shutdown();
  const ac6::renderer::NativeRendererStats stats = g_parallel_renderer.GetStats();
  std::lock_guard<std::mutex> status_lock(g_native_graphics_status_mutex);
  UpdateParallelRendererStatusLocked(g_parallel_renderer, stats);
}

bool InitializeParallelRenderer() {
  std::lock_guard<std::mutex> renderer_lock(g_parallel_renderer_mutex);

  ac6::renderer::NativeRendererConfig config{};
  config.feature_level = ac6::renderer::FeatureLevel::kBootstrap;
  config.max_frames_in_flight = 2;

  const bool initialized = g_parallel_renderer.Initialize(config);
  const ac6::renderer::NativeRendererStats stats = g_parallel_renderer.GetStats();
  {
    std::lock_guard<std::mutex> status_lock(g_native_graphics_status_mutex);
    UpdateParallelRendererStatusLocked(g_parallel_renderer, stats);
  }

  if (!initialized) {
    REXLOG_WARN("AC6 native renderer scaffold failed to initialize in parallel mode");
  }
  return initialized;
}

void TickParallelRendererFrame() {
  std::lock_guard<std::mutex> renderer_lock(g_parallel_renderer_mutex);
  const ac6::renderer::NativeRendererStats initial_stats = g_parallel_renderer.GetStats();
  if (!initial_stats.initialized) {
    return;
  }

  const ac6::d3d::FrameCaptureSnapshot frame_capture = ac6::d3d::GetFrameCapture();
  g_parallel_renderer.BeginFrame();
  if (!frame_capture.draws.empty() || !frame_capture.clears.empty() ||
      !frame_capture.resolves.empty()) {
    g_parallel_renderer.BuildCapturedFrame(frame_capture);
  } else {
    g_parallel_renderer.BuildBootstrapFrame();
  }

  const ac6::renderer::NativeRendererStats stats = g_parallel_renderer.GetStats();
  std::lock_guard<std::mutex> status_lock(g_native_graphics_status_mutex);
  UpdateParallelRendererStatusLocked(g_parallel_renderer, stats);
}

bool SamePassBinding(const ac6::d3d::ShadowState& left,
                     const ac6::d3d::ShadowState& right) {
  return left.render_targets[0] == right.render_targets[0] &&
         left.depth_stencil == right.depth_stencil &&
         left.viewport.width == right.viewport.width &&
         left.viewport.height == right.viewport.height;
}

bool SameReplayCandidate(const ReplayCandidateKey& left, const ReplayCandidateKey& right) {
  return left.rt0 == right.rt0 && left.depth_stencil == right.depth_stencil &&
         left.viewport_x == right.viewport_x && left.viewport_y == right.viewport_y &&
         left.viewport_width == right.viewport_width &&
         left.viewport_height == right.viewport_height;
}

