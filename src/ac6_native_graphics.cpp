uint32_t GetOutputScalePercent() {
  const std::string value = REXCVAR_GET(ac6_native_present_output_scale);
  if (value.empty()) {
    return 100;
  }
  int parsed = std::atoi(value.c_str());
  parsed = std::clamp(parsed, 50, 200);
  return static_cast<uint32_t>(parsed);
}

uint32_t ParseOptionalDimensionCvar(const std::string& value) {
  if (value.empty()) {
    return 0;
  }
  int parsed = std::atoi(value.c_str());
  if (parsed <= 0) {
    return 0;
  }
  return static_cast<uint32_t>(std::clamp(parsed, 1, 16384));
}

