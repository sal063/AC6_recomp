void ConfigureGraphicsBackend(rex::RuntimeConfig& config) {
  const bool bootstrap_enabled = REXCVAR_GET(ac6_native_graphics_bootstrap);
  if (bootstrap_enabled && REXCVAR_GET(ac6_native_graphics_auto_enable_capture) &&
      !REXCVAR_GET(ac6_render_capture)) {
    REXLOG_INFO(
        "AC6 native renderer bootstrap enabled render capture automatically for parallel scene ingestion");
    REXCVAR_SET(ac6_render_capture, true);
  }
  {
    std::lock_guard<std::mutex> lock(g_native_graphics_status_mutex);
    g_native_graphics_status = {};
    g_native_graphics_status.bootstrap_enabled = bootstrap_enabled;
    g_native_graphics_status.parallel_renderer_enabled = bootstrap_enabled;
    g_native_graphics_status.placeholder_present_enabled =
        IsDiagnosticPlaceholderPresentEnabled();
    const SwapRoutingMode routing_mode = GetSwapRoutingMode();
    const bool effective_native =
        REXCVAR_GET(ac6_native_present_enabled) ||
        REXCVAR_GET(ac6_native_present_disable_pm4) ||
        routing_mode == SwapRoutingMode::kNativeAuthoritative;
    g_native_graphics_status.native_present_enabled = effective_native;
    g_native_graphics_status.routing_mode = routing_mode;
    g_native_graphics_status.cadence_policy = GetCadencePolicy();
    g_native_graphics_status.configured_output_scale_percent = GetOutputScalePercent();
  }
  if (bootstrap_enabled) {
    InitializeParallelRenderer();
  } else {
    ShutdownParallelRenderer();
  }
#if REX_HAS_D3D12
  if (!bootstrap_enabled) {
    return;
  }

  config.graphics = std::make_unique<Ac6NativeGraphicsSystem>();
#else
  (void)config;
#endif
}

NativeGraphicsStatusSnapshot GetNativeGraphicsStatus() {
  SyncParallelRendererStatus();
  std::lock_guard<std::mutex> lock(g_native_graphics_status_mutex);
  NativeGraphicsStatusSnapshot snapshot = g_native_graphics_status;
  snapshot.bootstrap_enabled = REXCVAR_GET(ac6_native_graphics_bootstrap);
  snapshot.parallel_renderer_enabled = snapshot.bootstrap_enabled;
  snapshot.placeholder_present_enabled = IsDiagnosticPlaceholderPresentEnabled();
  const SwapRoutingMode routing_mode = GetSwapRoutingMode();
  snapshot.native_present_enabled =
      REXCVAR_GET(ac6_native_present_enabled) ||
      REXCVAR_GET(ac6_native_present_disable_pm4) ||
      routing_mode == SwapRoutingMode::kNativeAuthoritative;
  snapshot.routing_mode = routing_mode;
  snapshot.cadence_policy = GetCadencePolicy();
  snapshot.configured_output_scale_percent = GetOutputScalePercent();
  return snapshot;
}

