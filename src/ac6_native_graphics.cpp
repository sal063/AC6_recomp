uint32_t ComputeParityConfidenceScore(const NativeReplayPlanSummary& replay_plan,
                                      const SelectedPassPreviewData& selected_pass_preview,
                                      uint32_t* scene_pass_count, uint32_t* postfx_pass_count,
                                      uint32_t* ui_pass_count, uint32_t* missing_effects_counter,
                                      uint32_t* pixelation_counter) {
  const bool postfx_enabled = REXCVAR_GET(ac6_native_present_enable_postfx);
  const bool ui_compose_enabled = REXCVAR_GET(ac6_native_present_enable_ui_compose);
  uint32_t scene_count = replay_plan.pass_count;
  uint32_t postfx_count = postfx_enabled ? replay_plan.resolve_event_count : 0;
  uint32_t ui_count = (ui_compose_enabled && replay_plan.selected_pass_is_last) ? 1u : 0u;
  uint32_t missing_effects = 0;
  uint32_t pixelation = 0;
  uint32_t score = 100;
  if (!replay_plan.valid) {
    score = 70;
  } else {
    if (!replay_plan.present_candidate_matches_swap_size) {
      score = (score > 20) ? score - 20 : 0;
      ++pixelation;
    }
    if (postfx_enabled && !replay_plan.selected_pass_has_resolve && replay_plan.resolve_event_count) {
      score = (score > 18) ? score - 18 : 0;
      ++missing_effects;
    }
    if (!replay_plan.selected_pass_is_stable) {
      score = (score > 14) ? score - 14 : 0;
      ++pixelation;
    }
    if (!selected_pass_preview.summary.using_clear_fill &&
        replay_plan.selected_pass_clear_count > 0) {
      score = (score > 10) ? score - 10 : 0;
      ++missing_effects;
    }
  }
  if (scene_pass_count) {
    *scene_pass_count = scene_count;
  }
  if (postfx_pass_count) {
    *postfx_pass_count = postfx_count;
  }
  if (ui_pass_count) {
    *ui_pass_count = ui_count;
  }
  if (missing_effects_counter) {
    *missing_effects_counter = missing_effects;
  }
  if (pixelation_counter) {
    *pixelation_counter = pixelation;
  }
  return std::min<uint32_t>(score, 100u);
}

FloatColor DecodeArgbColor(uint32_t packed_color) {
  return {float((packed_color >> 16) & 0xFFu) / 255.0f,
          float((packed_color >> 8) & 0xFFu) / 255.0f,
          float(packed_color & 0xFFu) / 255.0f, 1.0f};
}

FloatColor MixColors(const FloatColor& left, const FloatColor& right, float t) {
  const float clamped_t = std::clamp(t, 0.0f, 1.0f);
  const float inverse_t = 1.0f - clamped_t;
  return {left[0] * inverse_t + right[0] * clamped_t,
          left[1] * inverse_t + right[1] * clamped_t,
          left[2] * inverse_t + right[2] * clamped_t, 1.0f};
}

FloatColor EnsureVisibleColor(const FloatColor& color, float min_luma) {
  FloatColor out = color;
  const float luma = out[0] * 0.2126f + out[1] * 0.7152f + out[2] * 0.0722f;
  if (luma < min_luma) {
    const float lift = min_luma - luma;
    out[0] = std::clamp(out[0] + lift, 0.0f, 1.0f);
    out[1] = std::clamp(out[1] + lift, 0.0f, 1.0f);
    out[2] = std::clamp(out[2] + lift, 0.0f, 1.0f);
  }
  out[3] = std::clamp(out[3], 0.0f, 1.0f);
  return out;
}

uint32_t PackImmediateColor(const FloatColor& color) {
  const auto pack_channel = [](float value) -> uint32_t {
    return uint32_t(std::clamp(value, 0.0f, 1.0f) * 255.0f + 0.5f);
  };
  const uint32_t r = pack_channel(color[0]);
  const uint32_t g = pack_channel(color[1]);
  const uint32_t b = pack_channel(color[2]);
  const uint32_t a = pack_channel(color[3]);
  return r | (g << 8) | (b << 16) | (a << 24);
}

uint32_t ScoreReplayPass(const ReplayPassCandidate& pass,
                         const rex::system::GraphicsSwapSubmission& submission,
                         bool is_last_pass) {
  uint32_t score = 0;
  if (pass.binding.viewport.width == submission.frontbuffer_width &&
      pass.binding.viewport.height == submission.frontbuffer_height) {
    score += 100;
  }
  if (pass.draw_count) {
    score += 40 + std::min<uint32_t>(pass.draw_count, 64);
  }
  if (pass.resolve_count) {
    score += 20;
  }
  if (pass.binding.render_targets[0]) {
    score += 10;
  }
  if (is_last_pass) {
    score += 25;
  }
  return score;
}

uint32_t ScoreObservedPass(const ac6::renderer::ObservedPassDesc& pass,
                           const rex::system::GraphicsSwapSubmission& submission,
                           bool is_last_pass) {
  uint32_t score = 0;
  if (pass.viewport_width == submission.frontbuffer_width &&
      pass.viewport_height == submission.frontbuffer_height) {
    score += 100;
  }
  if (pass.draw_count) {
    score += 40 + std::min<uint32_t>(pass.draw_count, 64);
  }
  if (pass.resolve_count) {
    score += 20;
  }
  if (pass.render_target_0) {
    score += 10;
  }
  if (is_last_pass) {
    score += 25;
  }
  return score;
}

void ApplyParallelFramePlanToReplayPlan(
    const ac6::renderer::FrontendFrameSummary& frontend_summary,
    const ac6::renderer::NativeFramePlan& frame_plan,
    const std::vector<ac6::renderer::ObservedPassDesc>& passes,
    const rex::system::GraphicsSwapSubmission& submission,
    NativeReplayPlanSummary* replay_plan) {
  if (!replay_plan || !frontend_summary.capture_valid || passes.empty()) {
    return;
  }

  replay_plan->frame_index = frontend_summary.frame_index;
  replay_plan->pass_count = frontend_summary.pass_count;
  replay_plan->draw_event_count = frontend_summary.total_draw_count;
  replay_plan->clear_event_count = frontend_summary.total_clear_count;
  replay_plan->resolve_event_count = frontend_summary.total_resolve_count;
  replay_plan->swap_size_match_pass_count = 0;
  replay_plan->largest_pass_draw_count = 0;
  replay_plan->first_pass_draw_count = passes.front().draw_count;
  replay_plan->last_pass_draw_count = passes.back().draw_count;

  for (const ac6::renderer::ObservedPassDesc& pass : passes) {
    replay_plan->largest_pass_draw_count =
        std::max(replay_plan->largest_pass_draw_count, pass.draw_count);
    if (pass.viewport_width == submission.frontbuffer_width &&
        pass.viewport_height == submission.frontbuffer_height) {
      ++replay_plan->swap_size_match_pass_count;
    }
  }

  uint32_t selected_pass_index = 0;
  bool selected_pass_valid = false;
  if (frame_plan.valid && frame_plan.present_stage.valid &&
      frame_plan.present_stage.pass_index < passes.size()) {
    selected_pass_index = frame_plan.present_stage.pass_index;
    selected_pass_valid = true;
  } else if (frontend_summary.selected_pass_index < passes.size()) {
    selected_pass_index = frontend_summary.selected_pass_index;
    selected_pass_valid = true;
  }
  if (!selected_pass_valid) {
    return;
  }

  const ac6::renderer::ObservedPassDesc& selected_pass = passes[selected_pass_index];
  replay_plan->selected_pass_index = selected_pass_index;
  replay_plan->selected_pass_score =
      ScoreObservedPass(selected_pass, submission,
                        (selected_pass_index + 1) == passes.size());
  replay_plan->selected_pass_start_sequence = selected_pass.start_sequence;
  replay_plan->selected_pass_end_sequence = selected_pass.end_sequence;
  replay_plan->selected_pass_draw_count = selected_pass.draw_count;
  replay_plan->selected_pass_clear_count = selected_pass.clear_count;
  replay_plan->selected_pass_resolve_count = selected_pass.resolve_count;
  replay_plan->selected_pass_is_last = (selected_pass_index + 1) == passes.size();
  replay_plan->selected_pass_has_resolve = selected_pass.resolve_count != 0;
  replay_plan->present_candidate_rt0 = selected_pass.render_target_0;
  replay_plan->present_candidate_depth_stencil = selected_pass.depth_stencil;
  replay_plan->present_candidate_viewport_x = selected_pass.viewport_x;
  replay_plan->present_candidate_viewport_y = selected_pass.viewport_y;
  replay_plan->present_candidate_viewport_width = selected_pass.viewport_width;
  replay_plan->present_candidate_viewport_height = selected_pass.viewport_height;
  replay_plan->present_candidate_matches_swap_size =
      selected_pass.viewport_width == submission.frontbuffer_width &&
      selected_pass.viewport_height == submission.frontbuffer_height;
  replay_plan->valid = true;
}

PlannedStagePreviewStage BuildPlannedStagePreviewStage(
    const ac6::d3d::FrameCaptureSnapshot& frame_capture,
    const std::vector<ac6::renderer::ObservedPassDesc>& passes,
    const ac6::renderer::PlannedPassReference& stage_reference) {
  PlannedStagePreviewStage stage;
  if (!stage_reference.valid || stage_reference.pass_index >= passes.size()) {
    return stage;
  }

  const ac6::renderer::ObservedPassDesc& pass = passes[stage_reference.pass_index];
  stage.valid = true;
  stage.pass_index = stage_reference.pass_index;
  stage.draw_count = pass.draw_count;
  stage.clear_count = pass.clear_count;
  stage.resolve_count = pass.resolve_count;
  stage.viewport_x = pass.viewport_x;
  stage.viewport_y = pass.viewport_y;
  stage.viewport_width = pass.viewport_width;
  stage.viewport_height = pass.viewport_height;

  bool has_first_clear = false;
  for (const ac6::d3d::ClearRecord& clear : frame_capture.clears) {
    if (!SequenceInObservedPass(clear.sequence, pass)) {
      continue;
    }
    if (!has_first_clear) {
      stage.first_clear_color = clear.color;
      has_first_clear = true;
    }
    stage.last_clear_color = clear.color;
    stage.using_clear_color = true;
    stage.sampled_clear_rect_count += clear.captured_rect_count;
    if (stage.clear_step_count < stage.clear_steps.size()) {
      SelectedPassClearStep& step = stage.clear_steps[stage.clear_step_count++];
      step.color = clear.color;
      step.rect_count = clear.captured_rect_count;
      step.rects = clear.rects;
    } else if (!stage.clear_steps.empty()) {
      SelectedPassClearStep& step = stage.clear_steps.back();
      step.color = clear.color;
      step.rect_count = clear.captured_rect_count;
      step.rects = clear.rects;
    }
  }

  for (const ac6::d3d::DrawCallRecord& draw : frame_capture.draws) {
    if (!SequenceInObservedPass(draw.sequence, pass)) {
      continue;
    }

    PlannedStageDrawSample sample{};
    sample.sequence = draw.sequence;
    sample.primitive_type = draw.primitive_type;
    sample.draw_count = draw.count;
    sample.flags = draw.flags;
    sample.texture_count = CountNonZeroEntries(draw.shadow_state.textures);
    sample.stream_count = CountBoundStreams(draw.shadow_state.streams);
    sample.sampler_count = CountBoundSamplers(draw.shadow_state.samplers);
    sample.fetch_constant_count =
        CountNonZeroEntries(draw.shadow_state.texture_fetch_ptrs);
    sample.viewport_x = draw.shadow_state.viewport.x;
    sample.viewport_y = draw.shadow_state.viewport.y;
    sample.viewport_width = draw.shadow_state.viewport.width;
    sample.viewport_height = draw.shadow_state.viewport.height;
    sample.indexed = draw.kind != ac6::d3d::DrawCallKind::kPrimitive;

    if (stage.draw_sample_count < stage.draw_samples.size()) {
      stage.draw_samples[stage.draw_sample_count++] = sample;
    } else if (!stage.draw_samples.empty()) {
      stage.draw_samples.back() = sample;
    }
  }

  return stage;
}

PlannedStagePreviewData BuildPlannedStagePreview(
    const ac6::d3d::FrameCaptureSnapshot& frame_capture,
    const ac6::renderer::NativeFramePlan& frame_plan,
    const std::vector<ac6::renderer::ObservedPassDesc>& passes) {
  PlannedStagePreviewData preview;
  if (!frame_plan.valid || passes.empty()) {
    return preview;
  }

  preview.present_from_selected_pass = frame_plan.present_from_selected_pass;
  preview.scene = BuildPlannedStagePreviewStage(frame_capture, passes, frame_plan.scene_stage);
  preview.postfx =
      BuildPlannedStagePreviewStage(frame_capture, passes, frame_plan.post_process_stage);
  preview.ui = BuildPlannedStagePreviewStage(frame_capture, passes, frame_plan.ui_stage);
  preview.valid = preview.scene.valid || preview.postfx.valid || preview.ui.valid;
  return preview;
}

NativeReplayPlanSummary AnalyzeReplayPlan(
    const ac6::d3d::FrameCaptureSnapshot& frame_capture,
    const rex::system::GraphicsSwapSubmission& submission) {
  NativeReplayPlanSummary summary;
  summary.frame_index = frame_capture.frame_index;

  std::vector<CapturedFrameEvent> events;
  events.reserve(frame_capture.draws.size() + frame_capture.clears.size() +
                 frame_capture.resolves.size());

  for (const auto& draw : frame_capture.draws) {
    events.push_back(
        {draw.sequence, CapturedFrameEvent::Type::kDraw, &draw.shadow_state});
  }
  for (const auto& clear : frame_capture.clears) {
    events.push_back(
        {clear.sequence, CapturedFrameEvent::Type::kClear, &clear.shadow_state});
  }
  for (const auto& resolve : frame_capture.resolves) {
    events.push_back(
        {resolve.sequence, CapturedFrameEvent::Type::kResolve, &resolve.shadow_state});
  }

  if (events.empty()) {
    return summary;
  }

  std::sort(events.begin(), events.end(),
            [](const CapturedFrameEvent& left, const CapturedFrameEvent& right) {
              return left.sequence < right.sequence;
            });

  std::vector<ReplayPassCandidate> passes;
  ReplayPassCandidate current_pass;
  bool current_pass_valid = false;
  auto flush_pass = [&]() {
    if (!current_pass_valid) {
      return;
    }
    passes.push_back(current_pass);
    current_pass = {};
    current_pass_valid = false;
  };

  for (const CapturedFrameEvent& event : events) {
    if (!event.shadow_state) {
      continue;
    }
    if (!current_pass_valid || !SamePassBinding(current_pass.binding, *event.shadow_state)) {
      flush_pass();
      current_pass.binding = *event.shadow_state;
      current_pass.start_sequence = event.sequence;
      current_pass_valid = true;
    }
    current_pass.end_sequence = event.sequence;

    switch (event.type) {
      case CapturedFrameEvent::Type::kDraw:
        ++summary.draw_event_count;
        ++current_pass.draw_count;
        break;
      case CapturedFrameEvent::Type::kClear:
        ++summary.clear_event_count;
        ++current_pass.clear_count;
        break;
      case CapturedFrameEvent::Type::kResolve:
        ++summary.resolve_event_count;
        ++current_pass.resolve_count;
        break;
    }
  }

  flush_pass();

  summary.pass_count = static_cast<uint32_t>(passes.size());
  if (!passes.empty()) {
    summary.first_pass_draw_count = passes.front().draw_count;
    summary.last_pass_draw_count = passes.back().draw_count;
  }

  uint32_t best_score = 0;
  uint32_t best_index = 0;
  bool best_index_valid = false;
  for (uint32_t i = 0; i < passes.size(); ++i) {
    const ReplayPassCandidate& pass = passes[i];
    summary.largest_pass_draw_count =
        std::max(summary.largest_pass_draw_count, pass.draw_count);
    bool matches_swap_size = pass.binding.viewport.width == submission.frontbuffer_width &&
                             pass.binding.viewport.height == submission.frontbuffer_height;
    if (matches_swap_size) {
      ++summary.swap_size_match_pass_count;
    }
    bool is_last_pass = (i + 1) == passes.size();
    uint32_t score = ScoreReplayPass(pass, submission, is_last_pass);
    if (!best_index_valid || score > best_score ||
        (score == best_score && i > best_index)) {
      best_index_valid = true;
      best_index = i;
      best_score = score;
    }
  }

  if (best_index_valid) {
    const ReplayPassCandidate& selected_pass = passes[best_index];
    summary.selected_pass_index = best_index;
    summary.selected_pass_score = best_score;
    summary.selected_pass_start_sequence = selected_pass.start_sequence;
    summary.selected_pass_end_sequence = selected_pass.end_sequence;
    summary.selected_pass_draw_count = selected_pass.draw_count;
    summary.selected_pass_clear_count = selected_pass.clear_count;
    summary.selected_pass_resolve_count = selected_pass.resolve_count;
    summary.selected_pass_is_last = (best_index + 1) == passes.size();
    summary.selected_pass_has_resolve = selected_pass.resolve_count != 0;
    summary.present_candidate_rt0 = selected_pass.binding.render_targets[0];
    summary.present_candidate_depth_stencil = selected_pass.binding.depth_stencil;
    summary.present_candidate_viewport_x = selected_pass.binding.viewport.x;
    summary.present_candidate_viewport_y = selected_pass.binding.viewport.y;
    summary.present_candidate_viewport_width = selected_pass.binding.viewport.width;
    summary.present_candidate_viewport_height = selected_pass.binding.viewport.height;
    summary.present_candidate_matches_swap_size =
        summary.present_candidate_viewport_width == submission.frontbuffer_width &&
        summary.present_candidate_viewport_height == submission.frontbuffer_height;
    summary.valid = true;
  }
  return summary;
}

SelectedPassPreviewData BuildSelectedPassPreview(
    const ac6::d3d::FrameCaptureSnapshot& frame_capture,
    const NativeReplayPlanSummary& replay_plan) {
  SelectedPassPreviewData preview;
  if (!replay_plan.valid) {
    return preview;
  }

  preview.summary.valid = true;
  preview.summary.draw_count = replay_plan.selected_pass_draw_count;
  preview.summary.clear_count = replay_plan.selected_pass_clear_count;
  preview.summary.resolve_count = replay_plan.selected_pass_resolve_count;

  bool has_first_clear = false;
  for (const auto& clear : frame_capture.clears) {
    if (!SequenceInSelectedPass(clear.sequence, replay_plan)) {
      continue;
    }

    if (!has_first_clear) {
      preview.summary.first_clear_color = clear.color;
      has_first_clear = true;
    }
    preview.summary.last_clear_color = clear.color;
    preview.summary.using_clear_fill = true;

    preview.summary.sampled_clear_rect_count += clear.captured_rect_count;
    if (preview.clear_step_count < preview.clear_steps.size()) {
      SelectedPassClearStep& step = preview.clear_steps[preview.clear_step_count++];
      step.color = clear.color;
      step.rect_count = clear.captured_rect_count;
      step.rects = clear.rects;
    } else {
      SelectedPassClearStep& step = preview.clear_steps.back();
      step.color = clear.color;
      step.rect_count = clear.captured_rect_count;
      step.rects = clear.rects;
    }
  }

  preview.summary.sampled_clear_color_count = preview.clear_step_count;
  return preview;
}

