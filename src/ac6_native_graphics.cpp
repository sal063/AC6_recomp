    const SwapRoutingMode routing_mode = GetSwapRoutingMode();
    const bool disable_pm4 = REXCVAR_GET(ac6_native_present_disable_pm4);
    const bool effective_disable_pm4 =
        disable_pm4 || routing_mode == SwapRoutingMode::kNativeAuthoritative;
    const bool effective_native_present_enabled =
        native_present_enabled || effective_disable_pm4 ||
        routing_mode == SwapRoutingMode::kNativeAuthoritative;
    const bool force_pm4_fallback = REXCVAR_GET(ac6_native_present_force_pm4_fallback);
    const bool allow_unstable = REXCVAR_GET(ac6_native_present_allow_unstable);
    const bool diagnostic_present_enabled = IsDiagnosticPlaceholderPresentEnabled();
